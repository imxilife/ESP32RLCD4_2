//
// 内置中文 16x16 点阵（世/界/中/文），1bpp 行主序，每行 2 字节，高位在左
// 与 Gui::drawBitmap 约定一致：stride=2, byteIndex=j*2+(i>>3), bit=7-(i&7)
//

#include "chinese_font.h"

namespace {

// 中 U+4E2D：方框加竖
static const uint8_t kGlyphZhong[32] = {
    0xFF, 0xFF, 0x81, 0x81, 0x81, 0x81, 0x81, 0x81, 0x81, 0x81, 0x81, 0x81,
    0x81, 0x81, 0x81, 0x81, 0x81, 0x81, 0x81, 0x81, 0x81, 0x81, 0x81, 0x81,
    0x81, 0x81, 0x81, 0x81, 0xFF, 0xFF,
};

// 文 U+6587：顶横 + 撇捺
static const uint8_t kGlyphWen[32] = {
    0x00, 0x00, 0x00, 0x00, 0x7F, 0xFC, 0x08, 0x20, 0x08, 0x20, 0x08, 0x20,
    0x10, 0x10, 0x10, 0x10, 0x20, 0x08, 0x20, 0x08, 0x40, 0x04, 0x40, 0x04,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
};

// 世 U+4E16：三横一竖
static const uint8_t kGlyphShi[32] = {
    0x00, 0x00, 0x7F, 0xFE, 0x02, 0x00, 0x02, 0x00, 0x02, 0x00, 0x7F, 0xFE,
    0x02, 0x00, 0x02, 0x00, 0x02, 0x00, 0x02, 0x00, 0x02, 0x00, 0x02, 0x00,
    0x02, 0x00, 0x02, 0x00, 0x02, 0x00, 0x00, 0x00,
};

// 界 U+754C：田 + 介
static const uint8_t kGlyphJie[32] = {
    0x00, 0x00, 0x3F, 0xF8, 0x20, 0x08, 0x20, 0x08, 0x3F, 0xF8, 0x20, 0x08,
    0x20, 0x08, 0x2F, 0xE8, 0x20, 0x08, 0x20, 0x08, 0x20, 0x08, 0x3F, 0xF8,
    0x20, 0x08, 0x20, 0x08, 0x20, 0x08, 0x00, 0x00,
};

}  // namespace

const uint8_t *ChineseFont_GetGlyph(uint32_t codepoint, int &width, int &height, int &strideBytes) {
    const uint8_t *glyph = nullptr;
    switch (codepoint) {
        case 0x4E2D: glyph = kGlyphZhong; break;  // 中
        case 0x6587: glyph = kGlyphWen;   break;  // 文
        case 0x4E16: glyph = kGlyphShi;   break;  // 世
        case 0x754C: glyph = kGlyphJie;   break;  // 界
        default:     break;
    }
    if (glyph) {
        width = 16;
        height = 16;
        strideBytes = 2;
    }
    return glyph;
}
