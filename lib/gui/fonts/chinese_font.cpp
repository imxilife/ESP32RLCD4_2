const uint8_t *ChineseFont_GetGlyph(uint32_t codepoint, int &width, int &height, int &strideBytes) {
    const uint8_t *glyph = nullptr;
    switch (codepoint) {
        case 0x4E2D: glyph = kGlyphZhong;  break;  // 中
        case 0x6587: glyph = kGlyphWen;    break;  // 文
        case 0x4E16: glyph = kGlyphShi16;  break;  // 世
        case 0x754C: glyph = kGlyphJie;    break;  // 界
        case 0x661F: glyph = kGlyphXing;   break;  // 星
        case 0x671F: glyph = kGlyphQi;     break;  // 期
        case 0x4E00: glyph = kGlyphYi;     break;  // 一
        case 0x4E8C: glyph = kGlyphEr;     break;  // 二
        case 0x4E09: glyph = kGlyphSan;    break;  // 三
        case 0x56DB: glyph = kGlyphSi;     break;  // 四
        case 0x4E94: glyph = kGlyphWu;     break;  // 五
        case 0x516D: glyph = kGlyphLiu;    break;  // 六
        case 0x65E5: glyph = kGlyphRi;     break;  // 日
        default:     break;
    }
    if (glyph) {
        width = 16;
        height = 16;
        strideBytes = 2;
    }
    return glyph;
}

// ── Font 适配器 ─────────────────────────────────────────────

const uint8_t *ChineseFont_GetGlyphFn(uint32_t codepoint,
                                       int &w, int &h, int &strideBytes,
                                       int &advanceX, const void * /*data*/) {
    const uint8_t *g = ChineseFont_GetGlyph(codepoint, w, h, strideBytes);
    advanceX = w;
    return g;
}

